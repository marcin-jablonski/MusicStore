    private void FilterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FilterButtonActionPerformed
        // TODO add your handling code here:
        FilterInfo filters = new FilterInfo();
        filters.setProductName(SearchTextField.getText());
        filters.setBody((String) BodyComboBox.getSelectedItem());
        filters.setPriceMin(MinPriceTextField.getText().equals("") ? Float.MIN_VALUE : Float.parseFloat(MinPriceTextField.getText()));
        filters.setPriceMax(MaxPriceTextField.getText().equals("") ? Float.MAX_VALUE : Float.parseFloat(MaxPriceTextField.getText()));
        
        MusicStoreBrowseProductsUI topFrame;
        topFrame = (MusicStoreBrowseProductsUI) SwingUtilities.getWindowAncestor(this);
        topFrame.FilterData(filters);
    }//GEN-LAST:event_FilterButtonActionPerformed


