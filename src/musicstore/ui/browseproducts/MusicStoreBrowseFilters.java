    private void setFilters(){
        Map<String, List<String>> filters = FiltersRetriever.GetFilters();
        setBodyComboBox(filters.get("Body"));
    }
    
    private void setBodyComboBox(List<String> bodies){
        bodies.stream().forEach(body -> {
            DefaultComboBoxModel model = (DefaultComboBoxModel) BodyComboBox.getModel();
            model.addElement(body);
        });
    }
